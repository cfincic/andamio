      <p class="Estilo13">Meyerhold decía:<br />
        “Un actor es un compositor en cada instante”</p>
      <p class="Estilo13">La propuesta de este seminario es un laboratorio de entrenamiento, improvisación y composición. El cuerpo como eje principal, como poética. La potencia creativa del movimiento. El entrenamiento físico desde la biomecánica de Meyerhold como punto de partida para entrar a la investigación de determinados elementos de improvisación y composición de un personaje. El autoconocimiento de nuestros propios limites como intérpretes y en relación al cuerpo del otro y al espacio.<br />
        El objetivo será llegar a la construcción de un personaje singular con el foco puesto en el cuerpo, la voz para luego abordar la escena.</p>
      <p class="Estilo13">Un actor, un cuerpo…<br />
        “Todo el cuerpo participa en cada uno de nuestros movimientos”- Meyerhold.</p>
      <p class="Estilo13">El cuerpo del actor, como entidad psico-física y emocional, es el instrumento, y por lo tanto, el elemento expresivo del actor/actriz.</p>
      <p class="Estilo13">Uno como actor busca asumir un compromiso con su cuerpo para lograr la creación escénica.<br />
        El trabajo del actor esta irremediablemente vinculado con su cuerpo, todo lo humano se enmarca siempre en un cuerpo vivido, en el espacio y en el tiempo, no es algo neutral. Es indispensable que cada actor logre tener una noción de su esquema corporal, sus diferentes partes del cuerpo que lo conforman en imágenes y la percepción también de esos segmentos corporales generando la auto-percepción para luego poder construir sobre si mismo, con el cuerpo del otro y en el espacio. El entrenamiento corporal no enseña a ser actor, ni tampoco a interpretar, es la base fundamental  para poder poner a prueba las propias intenciones, y superar las distancias, límites propios con nuestro instrumento y potenciar las capacidades creativas e ir mas allá para abordar la escena cada día como si fuera el primero y el único. </p>
      <p class="Estilo13">Nuestro cuerpo requiere una dedicación, un cuidado y un conocimiento ya que es nuestro instrumento, nuestra herramienta de trabajo. Mi cuerpo se presta para poder modificarlo, moldearlo y así poder crear un personaje. <br />
        La creación de un personaje desde la búsqueda corporal. Cuando ese cuerpo ya esta habitado por ese personaje, la palabra aparece como consecuencia. Si el cuerpo no esta presente, la palabra funciona solo como  texto y no esta la palabra atravesada por ese cuerpo.<br />
        La finalidad de este recorrido es lograr que el trabajo corporal ofrezca herramientas para la creación de personajes. El personaje no es solo acción y texto, es un cuerpo. El personaje es una forma que el cuerpo del actor adopta.</p>
      <p class="Estilo13">La actuación va ligada directamente con lo corporal tanto como lo vocal, no pueden ir separadas, son complementos fundamentales, son partes de un todo. Sin cuerpo ni voz no hay actor, sin actor no hay cuerpo ni voz.</p>
