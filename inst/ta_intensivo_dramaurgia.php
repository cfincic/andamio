        Primera Clase: Trazo Emotivo. Mediante pautas y ejercicios concretos, se busca motivar y generar disparadores para escribir. Que el propio alumno descubra que es lo que tiene para contar o que es lo que quiere contar.<br />
        Segunda Clase: Línea Irradiante. El alumno obtiene herramientas dramatúrgicas para analizar su escritura, generar hipótesis a futuro y seguir escribiendo su material. <br />
