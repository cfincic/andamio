        Sexta, Séptima y Octava clase: Trabajo intensivo de taller. Donde el alumno escribirá una obra corta, tomando como base disparadores   descubiertos en ejercicios de clases anteriores. También puede ser alguna idea nueva a desarrollar. Se leerán los textos en clase, y se darán devoluciones con la técnica aprendida.  El objetivo es ayudar al compañero a seguir escribiendo su material.<br />
        Objetivo del curso: Que el alumno “suelte la pluma” y pueda comenzar a escribir. Que pueda adquirir los conocimientos de dramaturgia que le permitirán contar una historia, “su primera obra”, o al menos descubrir mediante ejercicios, cual es la obra que tiene para contar. Muchas veces uno no escribe lo que quiere, sino lo que puede y ahí aparece la verdadera poética del dramaturgo.<br />
