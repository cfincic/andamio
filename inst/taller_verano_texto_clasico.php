      <p class="Estilo18">Seminario: La Actuación a partir del texto clásico.</p>
      <p class="Estilo18">Autores dramáticos a abordar: Lope de Vega, Calderón de la Barca, Tirso de Molina, Federico     García Lorca, Shakespeare.</p>
      <p><span class="Estilo18">A dictarse del 13 al 17 de febrero de 19 a 22 hs.</span> </p>
      <p class="Estilo13">Nota de difusión:</p>
      <p class="Estilo13">El Seminario está dirigido a estudiantes que hayan cursado niveles intermedios.</p>
      <p class="Estilo13">Nos abocaremos en trabajar textos del SIGLO DE ORO ESPAÑOL y algunos textos en verso de GRACÍA LORCA. Estos materiales nos llevan a la siguiente reflexión:<br />
        La pieza dramática no existe per se, detrás de ella hay un dramaturgo que planea una estrategia para contar una historia; decide cómo es el escandido de la información, las necesidades de los personajes, su forma de expresarlas, los momentos adecuados para que lo hagan, la interacción entre ellos y su entorno, las estrategias de manipulación que utilizan para conseguir sus objetivos y otras tantas cosas más… Todos estos factores son necesarios para esta clase de historias, que fueron escritas para ser actuadas. Este trabajo que hace el autor, en algunos casos parecen piezas de relojería, es para que los intérpretes lleven a escena su obra. Toda la información está ahí, en el texto.<br />
        En estos encuentros vamos a generar ejercicios que nos permitan comprender eso que está impreso para luego poder llevarlo al cuerpo.<br />
        Cada material pide un universo expresivo que les es propio ¿Qué es esto? Simple y en criollo “no se puede actuar todo de la misma manera”. No es lo mismo una tragedia griega que el neo realismo norteamericano de Tennessee Williams. No es lo mismo el absurdo, filoso, que propone Harold Pinter, que el denso universo que construye Griselda Gambaro; o el siglo de oro español y el surrealismo Lorquiano. A partir de eso nace una tarea cuasi detectivesca ¿Cuánta potencia expresiva es necesaria? ¿Qué pasa con la voz? ¿Qué pasa con mi background emocional – expresivo? Y no termina ahí… Luego debemos adentrarnos en uno de los problemas más interesantes ¿Cómo construyo momento a momento lo que allí sucede? ¿Cómo me sorprendo si ya sé qué va a pasar? (el actor sabe, el personaje no) ¿Cómo identifico cada micro acción? ¿Cómo la creo y la hago eficiente narrativamente? En fin… ¿Cómo se actúa estos materiales complejos?<br />
        Vamos a desarrollar ejercicios que nos permitan abordar estos problemas y varios más que van a ir apareciendo en nuestro tránsito por estos mundos.<br />
        No es un seminario difícil de tomar, pero exige compromisos; ensayos con compañeros, ensayos en casa o en la sala, estudiar textos y leer. En definitiva, lo que cualquier actor hace, o estudiante de actuación, tomar con alegría y rigor esto que tanto disfrutamos, actuar.</p>
      <p class="Estilo13">Propuesta formal:</p>
      <p class="Estilo13"> El seminario les acerca a los alumnos conceptos técnicos que les permitan el abordaje a materiales complejos. El siglo de oro español y otros materiales que trabajan con la rima y la métrica, por ejemplo, algunos de los textos de Lorca exigen al intérprete una serie de cuestiones técnicas que son necesarias adquirir antes de poder actuarlos. Parece una apreciación obvia, pero conlleva un número amplio de dificultades: <br />
        La memoria debe ser exacta o la métrica será destruida, por lo tanto, se atentaría contra la escucha del espectador que tuvo que acomodar su estructura auditiva al verso.<br />
        La articulación precisa coordinarse con la memoria, ya que el texto y su estructura gramatical es extraña para el intérprete de siglo XXI y le exige un gran esfuerzo al momento de transmitirlo, para ello entran en juego los labios, la lengua, el paladar, las cuerdas vocales y el manejo del aire. Es una coreografía que el cuerpo debe contener al momento de actuar.<br />
        La proyección de la voz que precisa cada pasaje del material, sus puntos de apoyo en el paladar y resonadores.<br />
        La comprensión del texto y su estructura, para poder anclarlo en los complejos movimientos de la acción.<br />
        El cuerpo entregado a los problemas de tensión que generan las complejas operaciones de interpretación.<br />
        La relación entre el cuerpo, el texto y la acción.<br />
        La relación entre la acción, la expresión y la energía (campo emocional).<br />
        Por sobre todo esto la acción dramática y la comprensión de su estructura y la estrategia narrativa que el autor propone.<br />
        Todo esto nos lleva a los desafíos que los intérpretes deben abordar antes de poder actuar estos materiales. Sobre eso se monta este seminario. Sobre el fracaso como potenciador de la expresión, sobre el trabajo arduo que le lleva a los actores antes de poder fluir sobre estos materiales complejos. </p>
