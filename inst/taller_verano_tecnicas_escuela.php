      <p class="Estilo18">Taller de Pedagogía Teatral: <br />
        Prof. Mariano Scovenna</p>
      <p class="Estilo18">“Técnicas y estrategias para llevar el teatro a la escuela”. </p>
      <p class="Estilo18">A dictarse los días 20 al 24 de febrero de 17 a 20 hs. </p>
      <p class="Estilo13">Fundamentación:</p>
      <p class="Estilo13">La extensión y la diversificación del arte teatral es un fenómeno que en los últimos años se ha expandido con gran fuerza.  La presencia del Teatro en los distintos niveles de la educación formal es una muestra del alcance de ello. <br />
        En nuestras escuelas, el teatro  encontró su espacio a partir de la siguiente idea: se puede aprender jugando.  A este postulado generatriz debemos defenderlo, alimentarlo y desarrollarlo para llegar a lugares insospechados. ¿Cómo lo hacemos? Ampliando los horizontes de alcance de cada una las actividades que proponemos en la clase y explorando describiendo explicando y correlacionando los conceptos, las prácticas y las especificidades del arte teatral junto a los chicos.  No debemos contentarnos con una jornada en la que los chicos sólo se divirtieron, debemos hacer todo lo posible para que jueguen y sientan, experimenten y reflexionen. <br />
        La permanencia del Teatro en nuestras escuelas, como espacio curricular viene de algún modo, a cumplir el sueño por el que Augusto Boal luchó mucho tiempo: Devolverle al pueblo los modos de producción teatral.  Ahora bien, para lograr ese objetivo es necesario que los chicos (parte fundamental del pueblo) conozcan, manejen y apliquen los conceptos sobre los que se levanta este corpus artístico. <br />
        El presente taller es una doble invitación. Por un lado nos propone pensar y repensar la manera en que hablamos de teatro con los chicos, mientras que  por otro lado nos convoca a poner en juego los conceptos teatrales y aplicarlos en las actividades que llevamos a cabo en el aula. <br />
        A veinte años de que el teatro entró al currículo escolar se vuelve pertinente revisar cuáles son los contenidos que justifican su enseñanza y transformar los conceptos, las explicaciones y las habilidades teatrales en contenidos escolares. Se hace necesario realizar una transposición didáctica de los conceptos teatrales en contenidos teatrales a enseñar. <br />
        Esta tarea es fundamental porque un niño que tiene las herramientas conceptuales para poder nombrar lo que hace, luego podrá reflexionar sobre su propia práctica.</p>
      <p class="Estilo13">Propósitos:</p>
      <p class="Estilo13"> Generar herramientas para transformar los conceptos y prácticas teatrales en contenidos escolares.<br />
        Construir criterios para seleccionar, secuenciar y organizar los contenidos escolares del Teatro haciendo foco en las necesidades de los chicos del siglo XXI. <br />
        Establecer puentes prácticos y teóricos entre los  procedimientos teatrales del campo artístico y su tratamiento en el aula para proyectar estrategias didácticas flexibles. <br />
        Abrir al juego los conceptos y procedimientos artísticos con el fin de construir prácticas de enseñanza. <br />
        Fomentar el debate y la construcción de acciones pedagógicas desde una mirada crítica.<br />
        Favorecer el análisis, vinculando el Teatro como espacio curricular con la escuela y la comunidad. </p>
      <p class="Estilo13">Contenidos:</p>
      <p class="Estilo13">Los contenidos se estructuran en tres núcleos temáticos que no se corresponden con un ordenamiento para su desarrollo. Se despliegan de modo transversal, en relación con los restantes núcleos. </p>
      <p class="Estilo13"> Núcleo Temático: La cultura teatral como caja de herramientas.<br />
        Los referentes de los contenidos escolares en el campo del Teatro- Los hacedores teatrales-  Las prácticas, los procedimientos y los conceptos artísticos- La función de las finalidades educativas.</p>
      <p class="Estilo13"> Núcleo Temático: La transposición didáctica.<br />
        Tensión entre los propósitos del teatro como lenguaje artístico y los del teatro como espacio curricular- Adecuaciones y  Versiones accesible para destinatarios singulares- Procesos de selección, secuenciación y organización para transformar un conocimiento producido en contextos sociales especializados- Las acciones pedagógicas.</p>
      <p class="Estilo13"> Núcleo Temático: El andamiaje y la confianza. <br />
        El acompañamiento docente- La confianza en que los aprendizajes son posible para que los sean- Las mediaciones- El docente como garante. </p>
      <p class="Estilo13">Metodología de trabajo:</p>
      <p class="Estilo13">Cantidad de encuentro: 5 sesiones de tres horas cada una.<br />
        Cada encuentro será teórico-práctico. Durante las jornadas se articularán diversas prácticas: exposiciones y explicaciones docentes, propuestas para la producción en acto  y puesta en juego, análisis de las propuestas, debates y revisión de producciones de los alumnos que sintetizan distintos aspectos de los contenidos.</p>
      <p class="Estilo13">Actividades:</p>
      <p class="Estilo13">Las actividades de cada encuentro están pensadas en tres momentos enlazados:<br />
        Durante el primer momento del encuentro realizaremos transformaremos los conceptos teatrales en definiciones que puedan ser comprendidas por los chicos de la educación primaria. Luego los configuraremos como contenidos escolares.<br />
        Durante el segundo momento del encuentro produciremos secuencias didácticas en las que se trabajen los contenidos. Presentaremos las propuestas.<br />
        Luego de producir y  poner juego el repertorio de actividades y estrategias didácticas para trabajar en el aula los conceptos y explicaciones teatrales elegidas analizaremos las propuestas y haremos reelaboraciones.  Por último socializaremos lo producido.</p>
      <p class="Estilo13">Requerimientos:</p>
      <p class="Estilo13">Espacio Amplio. </p>
      <p class="Estilo13">Evaluación:</p>
      <p class="Estilo13">La evaluación nos proporcionará información para la toma de decisiones. Los procesos de evaluación que se lleven a cabo nos permitirán, reflexionar, conceptualizar y hacer reelaboraciones que nunca serán construcciones homogéneas, ni definitivas.</p>
      <p class="Estilo13">La evaluación estará constituida por un conjunto de instrumentos que nos permitirán reflexionar sobre distintos aspectos de los aprendizajes. Serán utilizados los siguientes: auto-evaluación, evaluación grupal y devolución.</p>
