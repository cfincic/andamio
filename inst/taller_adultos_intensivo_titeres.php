        •	Relación títere-alumno titiritero <br />
        •	Acercamientos al guión dramático<br />
        •	Improvisación<br />
        <br />
        Desarrollar y caracterizar al personaje, mediante ejercicios sobre vivencias y situaciones ficticias.<br />
        Integrar herramientas para realizar la producción de la puesta en escena, de acuerdo con las habilidades e intereses de los alumnos. </p>
      <p class="Estilo16">OBJETIVOS GENERALES</p>
      <p class="Estilo16">El alumno apreciará, descubrirá y transitará el teatro de títeres a través de las distintas y variadas técnicas para su elaboración y su representación.<br />
