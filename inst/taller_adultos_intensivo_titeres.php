        INTRODUCCIÓN<br />
        Los Títeres, esos sagrados objetos inertes que poseen una vida prestada por un titiritero. El mismo los anima con su ritual maravilloso, colocándoles un pedacito de alma en sus cuerpitos de mentira, transformándolos en seres absolutamente vivos. Y así, ellos abren la puerta de nuestro corazón sin pedir permiso y a cuya inocencia nos entregamos desprejuiciosamente…<br />
        Ellos transgreden, desobedecen, se golpean infinitas veces, pero siempre se levantan, una y otra vez. Ante su fragilidad nos volvemos vulnerables. Ante su problemática nos comprometemos. Ante su inocencia nos sentimos inocentes.<br />
        Hay muchas descripciones de lo que es un títere:<br />
        •	Un muñeco dotado de palabra y de vida, un personaje que tiene autonomía, es un pequeño ser que permite dialogar, con un carácter propio y una vida independiente. </p>
      <p class="Estilo16">•	Un elemento plástico, especialmente construido para ser un personaje en una acción dramática, manipulado por un titerero que le da voz y movimiento.<br />
        •	Una marioneta o un títere es una pequeña figura o muñeco que se gobierna de manera que parezca que su movimiento sea autónomo. Se muestra en unos teatrillos llamados retablos. Su movimiento se efectúa con la ayuda de muelles, cuerdas, guantes, alambres, palos y otros utensilios adaptados a cada tipo de marioneta o títere.<br />
        •	Son pequeñas cosas que encuentran la magia de la vida, gracias a la intervención del artista. <br />
        •	Cualquier objeto movido en acción dramática. <br />
        •	Una figura inanimada que se mueve mediante el esfuerzo humano delante de un público.<br />
        •	Un títere puede ser cualquier cosa, un trapo, una escoba, un muñeco… solo hace falta que haya un manipulador que le dé vida. Un títere es el medio de comunicación entre un titiritero y el público.<br />
        •	Pequeñas figuras de pasta u otro materiales, vestidas y adornadas, que se mueven con alguna cuerda o artificio. <br />
        “Resumiendo, un títere es un objeto de comunicación - con apariencia humana o no -, que está animado/manipulado por una o varias personas que le dan movimiento y carácter, cumpliendo una función dramática.”<br />
        ¿Por que un taller de títeres?<br />
        El títere es un maravilloso aliado que  posibilitará al  alumno abrir nuevos canales de comunicación. <br />
        El títere permite a un alumno tímido la posibilidad de volverse repentinamente desafiante. Aquel que pronuncia pocas palabras mágicamente se lanza a hablar y aparecen aspectos secretos que no aparecían jamás en la vida corriente. <br />
        A través de los juegos se materializa en ellos diferentes situaciones que quizá  necesiten ser elaboradas. Por eso el Títere ayuda a liberar. <br />
        La verdadera esencia del Teatro de Títeres la constituye, el traspaso de la potencialidad creadora del alumno al títere. <br />
        La expresividad de un títere está determinada por su síntesis; es decir, por el gesto exacto y preciso que el alumno deberá encontrar y para ello, se comprometerá a transitar mediante el juego por sus emociones, descubrir y descubrirse. Para crecer hay que tener acceso a si mismo, habitarse.</p>
      <p class="Estilo16">El juego con títeres es un conocimiento que se construye y que ayuda a construir otros conocimientos. <br />
        El teatro de títeres es el lugar de reunión de varias disciplinas artísticas, entre las que se encuentran el modelado, la plástica, la pintura, la literatura, la expresión corporal, el canto y aún la música. <br />
        El alumno, apoyado en estas disciplinas, logrará mediante el uso de  la imaginación y la creatividad concretar el producto de este taller: Llegar a la elaboración de uno o varios títeres para representar y ejercitar.<br />
        Por lo tanto, este taller siempre será una invitación a sumergirse en este mundo imaginario, colorido y lleno de posibilidades de invención y juego, donde los alumnos podrán desplegar toda su imaginación e inventiva. <br />
        El contenido de este taller  proporcionará conocimientos sobre la clasificación de los tipos de títeres, así como de las técnicas para su elaboración y animación.</p>
      <p class="Estilo16">El taller se dividirá en tres partes: </p>
      <p class="Estilo16">1 - INTRODUCCION AL UNIVERSO DE LOS TITERES: </p>
      <p class="Estilo16">Breve acercamiento a la historia del teatro de títeres y sus diferentes técnicas, mediante el uso de audiovisual, imágenes y títeres realizados por artistas. Donde se aprenderán algunos  conceptos: Teatro de sombras, Teatro negro, bunraku, Títere de guante, teatro guiñol, muñecos de varilla, marionetas y fantoches; en fin distintos tipos y posibilidades. Definición de un títere. Encuentro y acercamiento al universo de los títeres desde la experiencia previa de los alumnos. <br />
          <br />
        2 - CONSTRUCCION Y MODELADO DE  TITERES: </p>
      <p class="Estilo16">Técnicas para su elaboración: Distintas posibilidades en materiales. Títere de papel maché,  tallado y modelado en goma espuma, reciclado de materiales descartables, telgopor, etc. <br />
        Dibujo de bocetos para la creación de un títere, acudiendo a su imaginación y creatividad. Utilización de distintas texturas para el enriquecimiento de la imagen construida, descubriendo las relaciones de tamaño entre las formas y  la creación de formas utilizando diferentes materiales. <br />
        Aplicación creativa de la pintura al títere. Diseño y confección del vestuario. Construcción de un retablo o espacio escénico, escenografía y utilería. </p>
      <p class="Estilo16">3 - TITERES EN ACCION:</p>
      <p class="Estilo16">Esta etapa implica más ejercicios y juegos con los títeres. Donde se  involucra varios  aspectos:  Desde lo literario, es decir las historias que se utilizaran como disparadoras, hasta las dramatizaciones poniendo en juego una participación cada vez más interesante de los participantes, transitando lo expresivo mediante los siguientes recursos:<br />
        •	El movimiento y la animación de los muñecos-títeres<br />
        •	La voz- Dicción. Respiración, sensibilización y creatividad vocal<br />
