      <p class="Estilo16">Fundamentación:<br />
      </p>
      <p class="Estilo16">Históricamente se ha considerado a las prácticas teatrales como acciones y productos que pertenecen exclusivamente al campo artístico. Mucho se avanzó en este campo, se han desarrollado tratados, teorías, técnicas y metodologías que dan cuenta del desarrollo que ha tenido nuestro arte. Pero desde fines del siglo XX el panorama se ha  complejizado. El teatro desembarcó en las escuelas y se conformó, además, como un espacio curricular en los distintos niveles de la educación formal.<br />
      </p>
      <p class="Estilo16">Este acontecimiento indica que el campo se ha extendido y diversificado, por lo que ya no estamos frente a dos denominaciones de una misma cosa (el teatro), sino que estamos ante dos versiones distintas de esa misma cosa.<br />
      </p>
      <p class="Estilo16">Maestros como Eisner y Mantovani manifiestan que, cuando el Teatro es colocado en otro ámbito y se relaciona con la educación, se vuelve necesario idear nuevos enfoques. Estos autores proponen que el nuevo punto de mira ya no es el del creador teatral sino el del educador, que tiene objetivos de carácter pedagógico y propósitos específicos para orientar a los sujetos que están en situación de tener una experiencia de aprendizaje.<br />
          <br />
        La Didáctica Especial del Teatro y los Profesores tenemos en nuestras manos la digna posibilidad de tomar posición ante las problemáticas del teatro en la educación y resolverlas diseñando proyectos, estrategias y materiales de enseñanza que ayuden a los niños y adolescentes a expandir y desarrollar sus mundos. Construyendo teatralidades que vinculen las experiencias educativas con sus vivencias presentes, anteriores y futuras.<br />
      </p>
