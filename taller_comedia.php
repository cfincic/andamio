     <blockquote>
       <p class="Estilo23">Taller de Commedia dell’Arte</p>
       <p class="Estilo9"><a href="cvs/cv_Roberto_Monzo.doc">Docente: Roberto Monzo (click para ver cv)</a></p>
       <p class="Estilo9"><strong><u>Presentación</u></strong><br />
         El taller es de una naturaleza eminentemente práctica,  debido a las características de la materia a trabajar. La Commedia dell’arte es  una disciplina que solo puede entenderse desde el cuerpo en acción.  El material teórico que se brindará durante  el taller tendrá como objetivo la profundización y mejor entendimiento del  proceso creador de la commedia, pero será de carácter accesorio. El trabajo se  dividirá en dos unidades, y la primera dará los contenidos que se utilizarán en  la segunda.</p>
       <p class="Estilo9"><strong><u>Unidad 1 </u></strong><br />
         Esta unidad tratará directamente sobre los personajes de la  Commedia dell’arte. A través del estudio de los personajes estudiaremos la  Commedia. Son ellos los que le dan vida, y en ella más que en cualquier otro  género teatral, definen los límites del campo. Se tratarán también ciertos contenidos  teóricos y otros que si bien no son específicos de la commedia, si son  esenciales para su desempeño. </p>
       <p class="Estilo9"><strong><u>Metodología</u></strong><br />
         Durante las clases habrá un bloque de Gesto, es decir exposición  de los tipos y características de cada personaje, con ejemplos concretos de  comportamiento y movimiento. Se utilizará también la improvisación, no como fin  en si mismo, sino como medio para un fin. Por último se llevará a cabo la  realización de canovacci, para poner en práctica los conocimientos adquiridos  en cada clase.</p>
       <p class="Estilo9"><strong><u>Duración</u></strong><br />
         El taller constará de una clase semanal de cuatro horas de  duración, dictadas a conveniencia de la institución, en un bloque único o dos  bloques por semana, durante tres meses.</p>
       <p class="Estilo9"><strong><u>Público</u></strong><br />
         Actores, clowns o bailarines con formación media o avanzada.</p>
       <p class="Estilo9"><strong><u>Contenidos</u></strong></p>
       <ul>
         <li class="Estilo9">Breve reseña histórica del género</li>
         <li class="Estilo9">Zanni. </li>
         <li class="Estilo9">Gli Innamorati</li>
         <li class="Estilo9">Il Capitano</li>
         <li class="Estilo9">Il Magnifico</li>
         <li class="Estilo9">Il Dottore</li>
         <li class="Estilo9">La Signora</li>
         <li class="Estilo9">Il Infarinato</li>
         <li class="Estilo9">Diferencias y similitudes entre Primo y secondo  zanni</li>
         <li class="Estilo9">Il Battocio</li>
         <li class="Estilo9">Manipulación de objetos</li>
         <li class="Estilo9">Golpes</li>
         <li class="Estilo9">Combate escénico</li>
         <li class="Estilo9">Repaso y especificidades de cada personaje</li>
         <li class="Estilo9">Elementos de acrobacia</li>
       </ul>
       <p>&nbsp;</p>
       <p class="Estilo9"><strong><u>Unidad 2</u></strong><br />
         El objetivo de esta unidad será la realización de un  espectáculo teatral en el que se pondrán en práctica los contenidos adquiridos  en la primera unidad. El rol del docente virará hacia la dirección, pero  considerando el guiar del desempeño de los actores dentro de los parámetros estéticos  y metodológicos de la Commedia dell’arte.</p>
       <h1 class="Estilo9 Estilo21">El material a trabajar será el  texto de un autor del período inmediatamente posterior a la commedia dell’arte,  como Molière o Carlo Goldoni, y será elegido por el docente dependiendo de las  necesidades y características del grupo.</h1>
       <h1 class="Estilo21">&nbsp;</h1>
       <h1 class="Estilo22"><u>Duración</u></h1>
       <h1 class="Estilo22">Cuatro horas semanales, una o dos  veces por semana, a conveniencia de la institución. La duración de esta unidad  dependerá de las características y circunstancias del grupo de trabajo, pero no  será de menos de tres meses.</h1>
