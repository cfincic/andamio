       <h1 class="Estilo17">TALLER DE ACTUACION PARA  PREADOLESCENTES</h1>
       <p class="Estilo17"><strong>FUNDAMENTOS:</strong></p>
       <p class="Estilo17">La preadolescencia es ese camino   inquietante y de grandes cambios que va  desde la niñez a la juventud.   Es la época donde los chicos se sienten muy  grandes para juegos de   niños pero demasiado pequeños para encajar en la  sociedad de los &quot;más   grandes&quot;.   Desde los 10 a 12 años en las nenas y de los 12 a los 14   años en los  nenes, aproximadamente, aparece la fase llamada   preadolescencia que en los  últimos años se ha adelantado, sus   características podemos decir que se ven  aparecer desde los 8 años,   especialmente en las nenas.  Los chicos entre los 8 y los 12 años,    transitan hoy una preadolescencía temprana ya que esta etapa no se   refiere solo  a cambios físicos sino,  también  pscicológicos.  Es una   época de ensueños  y dudas, de ir hacia delante y hacia atrás.   Es la   época de la rebeldía frente a los padres, por la búsqueda de la    independencia. Surge la necesidad de una vida social más intensa.  En   esta etapa el niño debe tener el espacio a  través del cual se le   permita ensayar su propia construcción de la vivencia  social y la   confianza para estructurar su normativa de convivencia.  Una   participación enriquecedora es aquella  donde se ofrecen principios y   valores de comportamiento. </p>
       <p class="Estilo17">DESTINATARIOS: </p>
       <p class="Estilo17">Jóvenes de 11 a 15 años   (aproximadamente) con o  sin experiencia.  Las clases respetarán el   proceso de aprendizaje,  individual y grupal. </p>
       <p class="Estilo17">OBJETIVOS: </p>
       <p class="Estilo17">Que los alumnos logren: </p>
       <ul type="disc">
         <li class="Estilo21">Descubrir valores explícitos e       implícitos   dentro de la actividad teatral, que favorezcan el desarrollo de       la   personalidad.</li>
         <li class="Estilo21">Valorar el hecho artístico, maximizando sus       posibilidades expresivas. </li>
         <li class="Estilo21">Desarrollar la expresión y la creatividad       explorando nuevas conductas individuales y grupales. </li>
         <li class="Estilo21">Valorar la importancia del trabajo grupal. </li>
         <li class="Estilo21">Aprender a trabajar dentro de un clima de respeto. </li>
       </ul>
       <p class="Estilo17">CONTENIDOS:</p>
       <p class="Estilo17">Integración. Desinhibición. Juegos.   Juegos  dramáticos. Imaginación. Comunicación. Cuerpo. Voz. Sonidos.   Ritmo. Impulso.  Historia. Estructura de la historia. Expresión   dramática. Producción Artística. </p>
       <p class="Estilo17">MODALIDAD  DE TRABAJO: </p>
       <p class="Estilo17">Las clases tendrán el formato de  &quot;taller&quot; - anual y  se dictarán una vez por semana ( 3 horas  reloj ). </p>
       <p class="Estilo17">  <br />
        EVALUACION: </p>
       <p class="Estilo17">Se  evaluará el proceso de   crecimiento individual y grupal  Se realizarán dos muestras obligatorias   a fin  de año en la sala de Andamio 90.</p>
