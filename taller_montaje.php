       <p><strong>Taller montaje para actores</strong></p>
       <p><strong>Profesor</strong> <a href="cvs/cv_Bernardo_Cappa.doc">Bernardo  Cappa (click para bajar cv)</a><br />
         Actuar para crear ficción.</p>
       <p>La voluntad del taller es tejer ficción con  las potencias de poéticas que los actores vayan desplegando.<br />
         Se le propondrá a cada integrante que elija  qué quiere actuar, esta elección será libre, puede elegir un personaje de una  obra de teatro, de un cuento, de una novela, una profesión, alguna celebridad,  en definitiva lo que estará eligiendo será una referencia, esa referencia le  permitirá desplegar su expresividad.<br />
         La organización del taller será la  siguiente: una primera etapa dónde cada uno trabajará su personaje sólo, no  para hacerlo bien, sino para que el actor descubra qué elije actuar de ese  personaje, qué reglas le propone para desplegar su poética.<br />
         Y una  segunda etapa dónde los personajes se irán vinculando, sin darle importancia al  ámbito pero sí a la forma de ese vínculo, así pueden cruzarse Hamlet con el  empleado de seguridad de un hospital público, de esta fricción surgirá una  tercera etapa de readaptación del personaje a los vínculos con los demás  personajes, de estos cruces surgirá el territorio de actuación, un espacio  posible, ficcional dónde convivan estos personajes, este espacio o marco  surgirá de los vínculos, no será pensado a priori, de la misma manera que lo que  cada actor actúe surgirá de lo que cada uno quiera actuar.<br />
         En este taller no se inventará primero la  ficción para que los actores la representen, sino que los actores generarán la  ficción con su propia voluntad de actuar. <br />
         Al final deberemos inventar un territorio,  un campo de juego marcado por las reglas que le encontramos para que sea lo más  atractivo posible.<br />
       </p>
