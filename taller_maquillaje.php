       <p><strong>Seminario de Maquillaje Artístico de  Caracterización.</strong></p>
       <p><a href="cvs/cv_Silvia_Zavaglia.doc">Docente y Caracterizadora: Silvia  Zavaglia (click para bajar cv)</a><a href="cvs/cv_Bernardo_Cappa.doc"></a></p>
       <p>Dirigido a todo aquel interesado en  las Artes Escénicas y Visuales. Directores, fotógrafos, actores, bailarines, coreógrafos,  artistas de circo, maquilladores, entre otros. </p>
       <p>Objetivos en esta cursada:</p>
       <p>Diseñar  y realizar Maquillajes de Caracterización.<br />
         Incorporar,  conceptos de claro oscuro, trazo y colorimetría. Aplicándolos a diseños que  serán vistos a corta y a gran distancia, como ser primeros planos, o grandes  escenarios como por ejemplo en el caso de la Ópera, Recitales, Eventos,  Desfiles, etc. Además de rudimentos básicos de FX, heridas, moretones, prótesis  y aplicación de postizos.<br />
         Se  realizarán caracterizaciones pre diseñadas a través de las cuales podremos  incorporar materiales y técnicas de aplicación que muestran con claridad los  efectos visuales y de composición a los que podemos recurrir a la hora de  realizar diseños de caracterización.  Trabajaremos  también metodologías y pasos a seguir a través de ejercicios de proceso  creativo apuntados al abordaje de un diseño propio, único y particular,  teniendo en cuenta  las características  generales en la composición de un personaje, aplicando y perfeccionando los  recursos técnicos adquiridos.</p>
       Contenidos teóricos  y prácticos
       <p><strong><u>Unidad 1</u></strong><strong>: </strong></p>
       <ul>
         <li>Presentación  de Materiales y Usos.</li>
         <li>Concepto  de Claro Oscuro. </li>
         <li>Bases  y Depresiones. </li>
         <li>Diseño  propio máscara blanco y negro. </li>
         <li>Envejecimiento</li>
         <li>Envejecimiento  progresivo.</li>
       </ul>
       <p>&nbsp;</p>
       <p><strong><u>Unidad 2</u></strong><strong>:    </strong></p>
       <ul>
         <li>Maquillajes  de Caracterización pre diseñados. Geisha, Chino, Tártaro, Diva, entre otros.</li>
         <li>Diseño  propio de Caracterización Modelo Animales.</li>
       </ul>
       <p><u>&nbsp;</u></p>
       <p><strong><u>Unidad 3</u></strong><strong>: </strong></p>
       <ul>
         <li>FX:  Heridas, Moretones, Prótesis, Aplicación de Postizos.</li>
         <li>Envejecimiento  de manos y cuerpo, marcar musculatura.</li>
         <li>Maquillaje  de Caracterización: elección de un personaje, y siguiendo una serie de pautas  crear y realizar un Diseño único y particular.</li>
         <li>Maquillaje  Final y correcciones.  </li>
       </ul>
