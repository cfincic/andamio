       <p>         La verdad escénica   es el rasgo singular que imprime el actor en su trabajo. Es la certeza  de lo irrepetible que sobrevuela toda la escena.  El seminario se propone una actualización del  trabajo del actor sobre sí mismo; su cuerpo como territorio, su emocionalidad  al servicio de la escena, su relación con el otro, con el espacio y con la  obra.  También se propone indagar sobre  lo falso. Sobre la falsificación. Sobre el artilugio. Sobre lo que se oculta y  fascina a la vez. La actuación como truco de magia. Como diría Orson Welles: “Hay  que saber distinguir un original de una falsificación. Saber distinguir una  buena o mala falsificación. Los tramposos siempre hemos existido.  Mi labor consiste en hacer parecer real, no  que la realidad tenga que ver con ella. Realidad es el cepillo de dientes que  nos espera en casa, el billete de autobús, un cheque, una tumba. Los mentirosos  profesionales esperamos ofrecer la verdad, el nombre ostentoso de esto es arte.  El arte es una mentira, una mentira que nos hace descubrir la verdad”.</p>
