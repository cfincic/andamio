     <blockquote>
       <p>Después de complicadas dolencias físicas, a los 88 años, falleció la creadora de salas, directora, docente, actriz y luchadora<br />
       </p>
       <p><span class="Estilo6">&quot;Mi   preocupación principal es la cultura.   Justamente mi conexión con el   teatro se da a partir de mi interés por la   cultura, a la que considero   la columna vertebral de un país.&quot;</span><br />
           <br />
         Este fue siempre el pensamiento y el   sentimiento   de Alejandra Boero, pionera y adalid del teatro   independiente   argentino, luchadora incansable de los derechos de los   creadores que   pugnaron por defender la libertad del arte y de los   artistas. Y fue   esta ideología la que marcó su trabajo profesional de   más de 60 años   en la actuación, la dirección y la docencia.<br />
         <br />
         Nunca bajó los brazos y cada año que   cumplía   parecía cargar sobre sus hombros sempiternos bríos juveniles   para   encarar nuevos proyectos. Con una salud que le jugó en varias     oportunidades malas jugadas, supo evadir a la muerte y sobrevivir     impulsada por esa dinámica de nunca darse por vencida, de nunca bajar     los brazos. Era tan fuerte, dio tantas batallas, que esa lucha     permanente era su compañía diaria. Sin embargo, la resistencia cultural     que propiciaba no la alejaba del trabajo, la creación, la discusión.<br />
         <br />
       </p>
       <p>Maestra   de cientos de actores, Boero   siempre tuvo a flor de piel esa   iniciativa que le permitió abrir varias   salas de teatro independiente   hasta recalar en la última, Andamio 90, a   la que convirtió en un   centro cultural y docente. “No me bajé del   caballo ni un solo día”,   dijo en su última entrevista a LA NACION, donde   se manifestó   preocupada por el destino del país, por la actividad   teatral y por los   jóvenes. “Si después de tantos años sigo durando es   porque los   jóvenes me dan vida”, af<br />
       </p>
       <p>Los primeros pasos <br />
           <br />
         Comenzó su actividad en el teatro La Máscara, en   1942, donde   debutó en la pieza “En algún lugar”, dirigida por Ricardo   Passano. En   1950 fundó, junto a Pedro Asquini, Nuevo Teatro, una   institución teatral   y cultural que marcó pautas renovadoras en el   planteo ético y estético   del teatro; construyó dos salas teatrales   más: Planeta y Lorange. Luego   intervino como actriz y directora en más   de 60 puestas en escena.<br />
         <br />
       </p>
       <p>Cada   una de las obras que elegía guardaba   relación con la época y los   acontecimientos sociales en los que le tocó   vivir. Le gustaban los   clásicos, pero esta preferencia no le impedía   pergeñar lo que ella   llamaba “una astracanada”. “Una cosa muy loca,   graciosa, libre, donde   nos damos el gusto tanto los directores como los   actores. Algo   humorístico, que nos pinta en el caos en que vivimos.”<br />
           <br />
       </p>
       <p>En   1992, con motivo de celebrar sus bodas   de oro con el teatro, Boero   declaró: “Tengo muchos sueños que cumplir   antes de irme de este   problemático mundo. No sueño con obras o   personajes, sino con   proyectos. Uno de ellos es formar un elenco casi   estable, para dar   lugar a una camada de actores jóvenes talentosos que   no encuentran el   espacio para desarrollarse. Quiero encender una llama   para alentar a   otra gente. Hace falta tener fe y hombros para soportar   el peso de   cada sueño”.<br />
           <br />
       </p>
       <p>Y   pudo cumplir con la mayoría de esos   sueños, aunque la realidad   económica, política y social la hacía   trastabillar en ese intento.   Pero, luego de gestiones con los   funcionarios del área, su elocuencia y   convicción obtenían de ellos   buenos resultados.<br />
           <br />
       </p>
       <p>La   asignatura pendiente siempre fue la   cultura como patrimonio de un   país. “La educación y la cultura es lo   único que hace a la identidad   de un país, y acá va a ser muy difícil   vivir. Se necesita una   verdadera política cultural de Estado.”<br />
           <br />
         Necesidad que nunca interesó a los   gobernantes   de turno. Ante cada cambio de gobierno, Alejandra Boero   levantaba   nuevamente las banderas de la cultura y enfrentaba desde un   pacifismo   ideológico a los funcionarios de turno. “Me halagan, me   felicitan y   cuando salgo de sus despachos se olvidaban de lo que habían     prometido”, señaló en más de una ocasión.<br />
         <br />
       </p>
       <p>Pero,   por el contrario, la indiferencia de   la burocracia la estimulaba para   buscar nuevas formas para colocar su   reclamo en primera fila.<br />
           <br />
       </p>
       <p>“Debo   reconocer que no me siento   frustrada. Cada etapa de mi vida la viví   como hay que vivirla. En cada   una de ellas tuve realizaciones,   encuentros y desencuentros. Miro para   atrás y veo que hicimos muchas   salas. No las hice sola, nadie puede   hacer las cosas solo. Eramos un   equipo, teníamos una fuerte capacidad   creativa, ilusiones, idealismo.   No soy pesimista, creo que las cosas se   pueden rescatar, pero todos   juntos, poniendo hombro con hombro. Debemos   volver a darles prensa a   los buenos sentimientos de la gente.”<br />
           <br />
           <br />
         En estas palabras se encuentra la   bisagra que   propulsó su condición de teatrista a un estadio superior, al   de una   representativa figura de la cultura, donde los elegidos tienen     reservado un lugar fundamental en el desarrollo de los pueblos.<br />
         <br />
       </p>
       <p>“Hay   que tener confianza en el hombre para   que se pueda desarrollar en lo   que sea –dijo en una oportunidad–. Ante   todo, hay que darle la   libertad para que realice aquello que quiere, no   aquello a lo que se   siente obligado porque le da de comer. Cuando el   hombre es él mismo,   es mejor persona. Y es lo suficientemente generoso   para reconocer a   otras buenas personas y les va a dar crédito para que   crezcan. Siempre   tuve fe en que podía construir una vida mejor. La vida   mejor consiste   en ser fiel a uno mismo, no engañarse, no mentir y no   disfrazarse de   nada.”<br />
           <br />
       </p>
       <p>Es   mucho lo que hay para decir de esta   gran mujer, pero no alcanzarían   las palabras, ni el espacio, para   registrar la actividad teatral de su   vida.<br />
           <br />
       </p>
       <p>En   los últimos años, la enfermedad la   venía acechando, pero ella la   enfrentaba con valentía. Aun en este   momento, en la hora de su muerte,   se puede decir que Alejandra seguirá   viviendo porque ya quedó   inscripta con moldes grandes en la historia del   teatro argentino. Por   todo lo que fue y lo que hizo, así será recordada   Alejandra Boero.<br />
           <br />
       </p>
       <p>Por Susana Freire De la Redacción de LA NACION</p>
