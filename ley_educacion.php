         <p align="justify"><span class="Estilo15"><strong>El teatro y la Ley de Educación  Superior</strong></span></p>
         <p align="justify"><span class="Estilo15"><br />
           La Educación Artística constituye un espacio curricular imprescindible en la  educación obligatoria y común de nuestro país. De acuerdo con la Ley de  Educación Nacional Nº 26.206, el objetivo de esta modalidad consiste en que  todos/as los/as alumnos/as -durante el transcurso de su escolaridad  obligatoria- tengan la oportunidad de desarrollar su sensibilidad y su  capacidad creativa en, al menos, dos (2) disciplinas artísticas.<br />
           En la Educación Secundaria, la modalidad artística ofrece formación  específica en Música, Danza, Artes Visuales, Plástica y Teatro. En la Educación  Superior, la formación específica es brindada en las escuelas especializadas en  artes.<br />
           http://www.mapaeducativo.edu.ar/Atlas/Educacion-Artistica<br />
            </span></p>
         <p align="justify"><span class="Estilo15"><strong>Educación Artística </strong></span></p>
         <p align="justify" class="Estilo15">El arte ha ocupado y ocupa en la actualidad un importante lugar en la vida  de los grupos sociales y en la construcción histórica del pensamiento. Es  manifestación, espectáculo, encuentro social, patrimonio cultural, espacio de  libertad para el desarrollo de la creatividad, vehículo de emociones y  sentimientos, pero también en tanto discurso que contiene una pluralidad de  interpretaciones acerca del mundo, constituye un campo de construcción y  expresión del pensamiento indispensable para la Sociedad.<br />
             <br />
           La Ley de Educación Nacional Nº 26.206 coloca en un lugar estratégico a la  Educación Artística y sus aportes para el Proyecto Político Nacional. En  sintonía con ella, el Ministerio de Educación nacional ha promovido la creación  de la Coordinación de Educación Artística Nacional, a efectos de promover las  políticas educativas que dicha ley plantea, especialmente con relación a la  transmisión y producción de conocimiento vinculado al arte y a la cultura.<br />
  <br />
           En el presente, la tradición de la lógica del pensamiento científico –junto con  los conocimientos culturales legitimados e incuestionables en tanto contenidos  curriculares que de él derivan– resulta insuficiente, o por lo menos parcial,  para conocer, abordar o analizar esa composición de elementos de diversa  naturaleza que conforman la realidad. Imágenes, sonidos, movimientos, gestos,  palabras componen discursos de naturaleza diversa, portadores de múltiples  significados y sentidos. El manejo de la metáfora, la pluralidad de lecturas  acerca de un mismo hecho, la apropiación de bienes culturales y el desarrollo  del pensamiento crítico y divergente son fundamentales a la hora de <em>interpretar  y desenvolvernos en la complejidad del mundo&nbsp; que vivimos.</em> <br />
  <br />
           Por otro lado, el arte pone de manifiesto la cuestión acerca de la diversidad y  la divergencia. La <em>competencia interpretativa</em> recorre los procesos de  producción y circulación de los discursos socio culturales. Allí, las  diferentes formas e instancias de lo artístico dialogan, tensionan, se definen  según su lógica singular. <br />
  <br />
           Ahora bien, el arte en tanto generador de discursos polisémicos, nunca es  totalmente agotado por las interpretaciones dominantes en una época dada. Al  contrario, eludir, ocultar, sugerir, metaforizar es propio del arte. Por tanto,  la actitud interpretativa, crítica, atraviesa la totalidad del proceso  artístico: desde el momento inicial de la producción de una obra hasta que  ésta, una vez materializada, inicia su circulación, el diálogo con el público.  En este sentido, el realizador también es un intérprete y un crítico que elige,  selecciona los recursos materiales y simbólicos, y los criterios para producir  su obra.&nbsp; <br />
  <br />
           Considerando la gravitación de las ideas de la modernidad en la construcción  del pensamiento artístico y en las representaciones sociales que en torno a él,  aún hoy se hayan presentes, resulta necesario resaltar que el arte, sus saberes  y capacidades, no forman parte de una excentricidad de la razón. El modo de  pensar estética y artísticamente - pensamiento por el cual se perciben y se  expresan síntesis, diferencias, singularidades e ideas de totalidad - no es  exclusivo de algunos pocos elegidos o talentosos sino que es parte de una  cualidad humana que necesariamente requiere ser desarrollada en todos los  sujetos.<br />
           En este sentido, <em>el arte es considerado un campo fundamental de  conocimiento</em> en tanto portador y productor de sentidos sociales y  culturales que se expresan en distintos formatos simbólicos estéticamente  comunicables, denomidados <strong>lenguajes artísticos</strong>. Entre ellos, pueden  mencionarse –considerando los desarrollos históricos y las presencias  contemporáneas–: <strong>música, plástica, teatro, danza, literatura, lenguajes  audiovisual y multimedial.</strong> </p>
         <p align="justify" class="Estilo15">El campo de la Educación Artística coloca, entonces, su atención en los  procesos de <strong>interpretación estético artística.</strong>&nbsp; Esta  última incluye saberes vinculados al desarrollo del pensamiento divergente y  creativo y al desarrollo&nbsp; de las capacidades espacio – temporales y de  abstracción, entre otras. <br />
           Pero, fundamentalmente se emparenta con los saberes y capacidades específicas  afines a la experiencia artística, es decir a la <strong>alfabetización de los  lenguajes artísticos</strong>, a los procesos de <strong>producción</strong> y  a los de <strong>análisis crítico</strong> relacionados con la&nbsp;  contextualización socio – cultural.&nbsp; Estas cuestiones suponen el  aprendizaje de saberes específicos que no son abordados por otros campos  disciplinares y que resultan fundamentales en la actuación ciudadana y en la  formación artístico y cultural profesional.&nbsp;&nbsp; <br />
           Es en parte por estas razones que la Educación Artística se presenta como un  campo clave de conocimiento a ser considerado por las políticas públicas  sociales, culturales, educativas y productivas en la actualidad. <br />
           La Educación Artística resulta entonces estratégica, como espacio curricular  imprescindible en la educación obligatoria y común de nuestro país, para la  distribución democrática de bienes materiales y simbólicos, y para la  construcción de la identidad social y política. Esto es, para la formación de  sujetos capaces de interpretar la realidad socio histórica con un pensamiento  crítico y de operar sobre ella, soberana y comprometidamente, con el conjunto,  para transformarla.</p>
         <p align="justify" class="Estilo15">Tres ejes prioritarios de la Educación Artística<br />
             <br />
             <strong>1-La Educación Artística General en la Educación Común y Obligatoria:</strong> su principal desafío plantea la transmisión de saberes y el desarrollo de las  capacidades vinculadas al arte y a la cultura, vinculados a sus diferentes  lenguajes - Música, Artes Visuales, Plástica, Teatro y Danzas, incluyendo  gradualmente otros lenguajes y especialidades actuales, tales como el lenguaje  audiovisual -, en pos de la interpretación crítica de los discursos en la  contemporaneidad, cuestión primordial para la construcción de la identidad y de  la soberanía. Su función resulta imprescindible para el logro de objetivos  educativo – estratégicos: la inclusión social, la construcción de ciudadanía y  su participación, el desarrollo del pensamiento divergente y la vinculación con  el mundo del trabajo. <br />
           Cabe mencionar que los saberes y capacidades relacionados con cada lenguaje  artístico no son transferibles de uno a otro. Aún cuando se reconozca un tronco  común de conocimiento artístico compartido, vinculado a la capacidad metafórica  y a la competencia interpretativa, los lenguajes artísticos expresan campos  disciplinares específicos, con procedimientos, técnicas y saberes propios que  no son intercambiables y que resultan sustantivos para una plena actuación en  sociedad.<br />
  <strong>2-La Educación Artística Específica, de Formación Profesional Media  y Superior:</strong> además de contribuir a la construcción ciudadana, su  principal desafío consiste en la formación de artistas y docentes –en tanto  sujetos políticos, comprometidos con sus realidades locales, regionales y  nacionales. Tal desafío persigue la incorporación efectiva, como política  pública de estado, de la producción artístico –cultural local y regional dentro  del proyecto socio– económico del país. Esto supone articular las instancias  formativas con las productivas, de distribución y circulación de los bienes  culturales; todos ellos constituyentes y constructores de la identidad, y al  mismo tiempo generadores de crecimiento socio – económico con justicia social. <br />
  <br />
  <strong>3-Arte, Educación y Cultura:</strong> su principal desafío se centra en  generar y articular políticas públicas para la promoción, el intercambio, la  comunicación y el conocimiento de las distintas culturas identitarias de los  grupos sociales y sus realidades locales, regionales y provinciales. Ello  implica garantizar las posibilidades de acceso y participación de los mismos en  programas de inclusión social y educativa, de extensión y difusión cultural,  como así también de promoción de emprendimientos productivos artístico  culturales.</p>
