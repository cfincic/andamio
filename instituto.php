     <p align="left" class="Estilo6 Estilo7"><strong>QUIÉNES SOMOS</strong></p>
     <blockquote>
         <p align="justify" class="Estilo8">Somos   una Institución de nivel terciario, que fomentamos la   Educación   integrando valores sociales y culturales mediante: La   formación   académica, humanística y científica. La investigación, la   docencia, la   enseñanza y el desarrollo de las artes y las ciencias. La   excelencia   educativa como meta compartida por la Comunidad Académica. La     formación ética, moral y solidaria que respalden y distingan su     desempeño humano profesional. La formación de profesionales capacitados     para establecer la integración entre Comunicación, Imagen y     Organización.</p>
         <p align="justify" class="Estilo9">¿Quiénes nos presiden y avalan?</p>
         <p align="justify" class="Estilo8">Nos   destaca la actividad Educativa que originalmente desarrolló       Alejandra Boero, nuestra Fundadora, con toda una vida dentro del     ámbito   teatral y cultural y casi 60 años dedicada a la representación y       dirección teatral, Avalado esto además por su prestigio y   dedicación,     han hecho posible que el Espacio Cultural   Andamio´90, haya cumplido   en el año 2010, 20 años de vida, transitando   los caminos del Arte con   mayúsculas. El rector del Colegio Superior   de Artes del Teatro y la   Comunicación, el licenciado Alejandro Samek,   lleva casi 40 años dedicado   al campo de las artes y esa sólida   expreriencia se ve refljada en las   carreras y los contenidos, que ha   sido diseñado, y han sido aprobadas   oficialmente en su totalidad.</p>
         <p align="justify" class="Estilo9">¿Qué te brindamos?</p>
         <p align="justify" class="Estilo8">Asumimos   el compromiso social de otorgarte una formación que   acompaña a estos   tiempos, para que puedas integrarte vocacionalmente a   la Industria   Cultural y Social, desde las distintas carreras que te   ofrecemos,   capacitándote competentemente para operar con calidad y   eficiencia en   la realidad actual. Seminarios de las mejores Escuelas de   Londres y   Nueva York. Convenios con Universidades Nacionales de   reconocimiento   Internacional.</p>
         <p align="justify" class="Estilo9">¿Cómo es nuestra metodología?</p>
         <p align="justify" class="Estilo8">Encontrarás   una metodología pedagógica interdisciplinaria con   relación a los   objetivos y los contenidos de las distintas carreras que   ofrecemos,   como también ofertas extracurriculares de seminarios,   investigaciones y   acceso a fuentes de información, documentación y   bibliotecas   relacionadas con la producción de conocimientos que te   permitirán   estar actualizado, nutrir, estimular y orientar tu vocación y     profesión con los nuevos desarrollos mundiales en el campo de la     Comunicación, los que fortalecerán tu liderazgo.</p>
         <p align="justify" class="Estilo9">¿Cuál es nuestro objetivo?</p>
         <p align="justify" class="Estilo8">Buscamos   la excelencia como una meta compartida por toda la   Comunidad   Educativa. Y para alcanzarla utiliza métodos y tecnologías     actualizadas que permiten evaluar la educación y formación impartida, ya     que de ello depende la calidad de conocimientos con los que nuestras     futuras generaciones entrarán al mundo laboral.</p>
         <p align="justify" class="Estilo9">¿Por qué elegirnos?</p>
         <p align="justify" class="Estilo8">Reconocemos   la importancia crítica del Hombre como factor   diferencial en la   sociedad y en la empresa moderna, en un contexto donde   la   incertidumbre y la vertiginosidad de los cambios son desafíos     constantes para el liderazgo; la sabiduría y el conocimiento son     indispensables para desempeñarse con capacidad crítica en la búsqueda,     evaluación y aplicación de la información que permita con eficiencia el     logro de los objetivos planteados en cada una de las ofertas   educativas   que tenemos para ofrecerte.</p>
