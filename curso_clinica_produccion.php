     <blockquote>
       <p><strong>         Clínica de producción para el Desarrollo de proyectos  artísticos</strong></p>
       <p><br />
        DOCENTES Rosalía Celentano y Paula Baró</p>
       <p><strong><a href="cvs/cv_ROSALIA_ CELENTANO.doc">CV ROSALIA CELENTANO</a></strong> <a href="cvs/cv_ROSALIA_ CELENTANO.doc">(click para ver cv)</a><br />
         <br />
         <strong><a href="cvs/cv_PAULA_BARO.doc">CV PAULA BARÓ</a></strong> <a href="cvs/cv_PAULA_BARO.doc">(click para ver cv)</a></p>
       <p> FORMATO Teórico Práctico- 1 hora cátedra  teórico/ 1 hora cátedra práctico<br />
         DURACIÓN 12 encuentros de 2 y 1/2  horas cátedra cada vez. 30 horas en total<br />
         Dirigido a: Personas que desarrollen  actividades artísticas o con interés en dicha área que deseen capacitarse en la  gestión, diseño y producción de proyectos culturales.<br />
       </p>
     <p><em>Teatro - Danza – Circo – Música – Seminarios – Eventos – Festivales -  Ciclos</em>&nbsp;<br />
           <br />
           <strong>Objetivo</strong></p>
       <ul>
         <li>Ampliar las  capacidades creativas en búsqueda de soluciones innovadoras que permitan  optimizar los recursos con los que se cuenta.</li>
         <li>Que los alumnos  presenten un proyecto y lo desarrollen en el  tiempo de la cursada.</li>
       </ul>
       <p><strong>Metodología</strong><br />
         La metodología utilizada es aula taller. En la primer parte de la clase  se exponen conceptos fundamentales de la producción en intercambio con los  alumnos.<br />
         En la segunda parte los alumnos aplican los conocimientos adquiridos a  sus proyectos individuales y se resuelven dudas sobre la práctica concreta.<br />
  <strong>Contenidos</strong><strong> </strong><br />
  &nbsp; <em>Las clases abordan contenidos teóricos fundamentales que serán  desarrollados en forma práctica sobre los proyectos que los asistentes  propongan en la clínica.</em> <br />
  &nbsp;<em>Taller en donde la producción se presenta como un vehículo que  posibilita la concreción de proyectos, pero fundamentalmente como una actividad  creativa en sí.</em>&nbsp; </p>
       <ul type="disc">
         <li>Reflexión y definición del rol de producción -       producción artística, producción ejecutiva, producción general, gestión       cultural, etc.-&nbsp; </li>
         <li>Dialéctica Creación / Producción&nbsp; </li>
         <li>Lo Público y Lo Privado &nbsp; </li>
         <li>La producción como generadora de puestos de       trabajo&nbsp; </li>
         <li>La formación de los Gestores Culturales&nbsp; </li>
         <li>Proyección económica: Costos y Ganancias a       tener en cuenta.&nbsp; </li>
         <li>Diseño y contratación de equipo       artístico&nbsp; </li>
         <li>Diseño y contratación de equipo técnico&nbsp; </li>
         <li>El producto: su comercialización e       inserción&nbsp; </li>
         <li>La captación de públicos&nbsp; </li>
         <li>Diseño de campañas de difusión, prensa,       comunicación&nbsp; </li>
       </ul>
       <p><em>Clínica o laboratorio donde los participantes exponen dudas y  necesidades en relación a un proyecto real en el que se encuentran trabajando.  En caso de no tener aún un proyecto, las docentes le asignaran uno a  desarrollar. Los proyectos pueden estar en diversas etapas de desarrollo y  serán expuestos ante el grupo para encontrar las mejores soluciones.</em>&nbsp; </p>
       <ul type="disc">
         <li>Análisis de las Condiciones Socio-Políticas en       las que se desarrolla un Proyecto Cultural.&nbsp; </li>
         <li>Estrategias y conceptos para el diseño de un       proyecto.&nbsp; </li>
         <li>Los Espacios Concretos del Desarrollo de los       Proyectos.&nbsp; </li>
         <li>Etapas para la concreción del mismo:       Preproducción- Producción- posproducción.&nbsp; </li>
         <li>Estrategias y posibilidades para la búsqueda       de Financiamiento- Formas actuales de financiación de la cultura. La       búsqueda de modelos específicos.&nbsp; </li>
         <li>Estrategias en la Presentación de       Proyectos.&nbsp; </li>
         <li>Alternativas para la Diversificación       de Ofertas Escénicas.&nbsp; </li>
       </ul>
       <p><strong>Bibliografía</strong><br />
         Heras, Guillermo Artículos Varios<br />
         García Canclini - Moneta Carlos Artículos Varios<br />
         Cimarro, Jesus “Producción teatral”<br />
         Dossier diseñado por las docentes donde se incluye  legislación actual, modelos de documentación de la asociación argentina de  actores, argentares, solicitudes de subsidios INT, FNA, FMAyC, MECENAZGO,  Prodanza y Proteatro y modelos de presupuesto, entre otros.</p>
       <p><strong>Evaluación</strong><br />
         La evaluación es constante ya que se les exige a  los alumnos entregas semanales en relación a lo planteado en cada clase.<br />
         Como cierre de la cursada se realiza una clase  especial con una autoevaluación por parte de cada uno de los participantes  (docentes y alumnos) y evaluación sobre el trabajo de los compañeros.<br />
