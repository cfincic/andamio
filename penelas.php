       <p align="center"><span class="Estilo10" id="yui_3_2_0_1_1346349197436377">PRESENTACIÓN DEL LIBRO</span></p>
       <p align="center" class="Estilo10"><span id="yui_3_2_0_1_1346349197436338">EL HOMBRE COMO LA SAL DEL AGUA</span></p>
       <p align="center"><span class="Estilo10" id="yui_3_2_0_1_1346349197436383">de Lisandro Penelas</span></p>
       <p align="center"><span class="Estilo10" id="yui_3_2_0_1_1346349197436409">El lunes 10 de septiembre a las 19.30 hs</span></p>
       <p align="center" class="Estilo10"><span id="yui_3_2_0_1_1346349197436415">se llevará a cabo la presentación del libro publicado este año por Editorial Dunken</span></p>
       <p align="center" class="Estilo10"><span id="yui_3_2_0_1_1346349197436421">en el teatro Andamio 90, Paraná 660</span></p>
       <p align="center" class="Estilo10">Leerá poemas Roberto Monzo y la música estará a cargo de Matías Macri y Federico Justo</p>
       <p align="center"><img src="images/penelas.jpg" width="661" height="960" /></p>
       <div class="Estilo10">Lisandro Penelas nació en Buenos Aires  en 1983. Es actor, director y profesor de actuación recibido de CoSATyC.  </div>
       <div class="Estilo10">Trabajó en más de veinte obras de teatro, y en varios cortos y  largometrajes. Se formó con docentes como Claudio Tolcachir, Luciano  </div>
       <div class="Estilo10">Suardi, Javier Daulte y Marcelo Katz, entre otros, y en 2009 fue becado para formar parte del Cultural Visitors Program for Directors en Estados Unidos.</div>
       <div class="Estilo10"><br />
       </div>
       <div class="Estilo10" id="yui_3_2_0_1_1346600559856293">Paralelamente a su trabajo en el mundo de la escena estuvo siempre en contacto con su otra vocación: la escritura. Cursó la carrera de Artes Combinadas en la Universidad de Buenos Aires. En 2000 publicó su primer cuento, Ubi sunt?, por   Ediciones del valle y con sólo veinte años su cuento La foto fue seleccionado por Igriega Movimiento Cultural para formar parte de una antología de relatos publicada en Sevilla, España. Ese mismo año su estudio Chaplin: la commedia dell'arte llega al cine integró el libro ¿Imágenes del nuevo siglo? de Editorial TEALHI. En 2006 colaboró en la redacción de varios volúmenes de la colección Protagonistas de la Cultura que publicara el Diario   La Nación junto a Editorial Aguilar. En 2009 su poema Ana fue incluido en El libro de los talleres de Editorial Dunken. Como dramaturgo, realizó la adaptación teatral de ¿Querés hacer el favor de callarte, por favor? basada en tres relatos de Raymond Carver.</div>
       <div class="Estilo10"><br />
       </div>
       <div><span class="Estilo10" id="yui_3_2_0_1_1346600559856296">La publicación de este libro le da la bienvenida a este joven escritor argentino, que presenta aquí su primer poemario,   donde puede reconocerse un amplio abanico de influencias: la generación del 27, los autores estadounidenses contemporáneos y la literatura argentina y latinoamericana del último sig</span><span class="Estilo10" id="yui_3_2_0_1_1346600559856296"></span><span id="yui_3_2_0_1_1346600559856296">lo.</span></div>
