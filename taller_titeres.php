     <blockquote>
       <p><strong>TALLER</strong></p>
       <p>Docentes:</p>
       <p> <a href="cvs/cv_FRANCISCO_RAMIREZ.doc">Francisco Ramírez (click para ver cv)</a></p>
       <p><a href="cvs/cv_ariel.doc">Ariel Alejandro Nesterczuk (click para ver cv)</a><a href="cvs/CV Natalia Carmen Casielles.doc"></a></p>
       <p>“DE LA CREACION  DEL  TÍTERE, AL JUEGO CON TITERES “<br />
         “Para q pueda ser, he de ser otro, salir de mi, <br />
         buscarme entre los otros, los otros que no son si yo no existo, <br />
         los otros q me dan plena existencia”<br />
        Octavio Paz</p>
       <p> <br />
          <strong>INTRODUCCIÓN</strong></p>
       <p> <br />
         Los Títeres, esos sagrados objetos inertes que poseen una vida prestada por  un titiritero , que los anima con su ritual maravilloso, colocándoles un  cachito de alma en sus cuerpitos de mentira, transformándolos en seres  absolutamente vivos que abren la puerta de nuestro corazón sin pedir permiso y  a cuya inocencia nos entregamos desprejuiciosamente…<br />
         Ellos transgreden, desobedecen, se golpean infinitas veces, pero siempre se  levantan, una y otra vez. Ante su fragilidad nos volvemos vulnerables. Ante su  problemática nos comprometemos. Ante su inocencia nos sentimos inocentes.<a name="21" id="21"></a><br />
          <strong> “Un títere es un objeto de  comunicación -con apariencia humana o no-, que está animado-manipulado por una  o varias personas que le dan movimiento y carácter, cumpliendo una función dramática.”</strong> <br />
                     </p>
       <div>
         <p align="center">Este      taller siempre será una invitación a sumergirse en este maravilloso mundo del      teatro de objetos, plagado de colorido y lleno de posibilidades, donde los alumnos      podrán desplegar toda su  imaginación      e inventiva.</p>
       </div>
        
       </p>El contenido de este taller   proporcionará conocimientos sobre la  clasificación de los tipos de títeres, así como de las técnicas para su  elaboración y animación.
       <p>El taller  se dividirá en tres partes</p>
       <p><strong>1-INTRODUCCION  AL UNIVERSO DE LOS TITERES</strong>: </p>
       <p>Breve acercamiento a la  historia del teatro de títeres y sus diferentes técnicas, mediante el uso de  audiovisual, imágenes y títeres realizados por artistas. Donde se aprenderán algunos   conceptos: Teatro negro, bunraku, <a href="http://www.titerenet.com/2004/01/10/titere-de-guante/">Títere de  guante</a>, teatro guiñol, muñecos de varilla, marionetas, etc.; en fin  distintos tipos y posibilidades. Definición de un&nbsp; títere. Encuentro y  acercamiento al universo de los títeres desde la experiencia previa de los alumnos.  &nbsp;<br />
  &nbsp;&nbsp;&nbsp;&nbsp; <u> </u><br />
         <br />
  <strong>2-CONSTRUCCION  Y MODELADO DE  TITERES: <br />
  </strong><br />
         Técnicas para su  elaboración: Distintas posibilidades en materiales. Títere de papel maché,  tallado y modelado en goma espuma, reciclado  de materiales descartables, telgopor, etc. <br />
         Dibujo de bocetos para  la creación de un títere, acudiendo a su imaginación y creatividad. Utilización  de distintas texturas para el enriquecimiento de la imagen construida, descubriendo  las  relaciones de tamaño entre las formas y  la creación de formas utilizando diferentes  materiales. Aplicación  creativamente de la pintura al títere. Diseño y confección del vestuario.  Construcción de un retablo o espacio escénico, escenografía y utilería. </p>
       <p><strong>3-TITERES EN ACCION:</strong></p>
       <p>Esta etapa implica más ejercicios  y juegos con los títeres. Donde se  involucra varios   aspectos:&nbsp;Desde lo literario, es  decir las historias q se utilizaran como disparadoras, hasta las dramatizaciones  poniendo en juego una participación cada vez más interesante de los  participantes, transitando lo expresivo, Jugando a ser Titiritero mediante  los siguientes recursos: <br />
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- El movimiento y la animación de los  muñecos-títeres<br />
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- La voz- Dicción. Respiración, sensibilización y  creatividad vocal<br />
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;- Características y Construcción de  personajes&nbsp;(distintas concepciones) <br />
  &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;-  Relación títere-alumno titiritero <br />
         - Acercamientos al guión dramático<br />
         - Improvisación<br />
         - Puesta en escena<br />
         En fin, proporcionar  los fundamentos teórico-prácticos para que los alumnos comprendan y apliquen de  manera correcta las posiciones y movimientos del títere y del manipulador,  también realizar ejercicios de creatividad sonora mediante la emisión de  vocales, fonemas y juegos de expresión verbal.<br />
         Desarrollar y  caracterizar al personaje, mediante ejercicios sobre vivencias y situaciones  ficticias.</p>
       Integrar  herramientas para realizar la producción de la puesta en escena, de acuerdo con  las habilidades e intereses de los alumnos.
       <p class="Estilo23">&nbsp;</p>
