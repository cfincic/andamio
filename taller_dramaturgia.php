       <p>Docente: <a href="cvs/CV Natalia Carmen Casielles.doc">Natalia Carmen Casielles</a> (click para ver cv)<br />
           <br />
         Duración: Trimestral<br />
  <br />
         Cómo se construye un texto dramático desde el mínimo hasta el máximo  detalle, creando desde allí a sus personajes, sus historias y su universo  propio; haciendo hincapié en la mirada poética de cada autor. Se tomará como  punto de partida ejercicios de creación personal para reforzar los conceptos  planteados e incentivar la observación y la escritura que acompañaran los  contenidos vistos en cada uno de los módulos planteados en el taller<strong>.<u> </u></strong> </p>
