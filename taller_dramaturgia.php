               <p><strong>VEROSIMILITUD Y UNIVERSO POÉTICO</strong> </p>
             <p>La    importancia de lo verosímil. Lo real no existe en el arte pero si la búsqueda    de verdad, de lo verosímil de la historia. </p>
             <p>No importa    cuán extraña sea nuestra historia y nuestros personajes, siempre y cuando    sintamos que en el universo creado existe. </p>
