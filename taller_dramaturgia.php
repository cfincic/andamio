               <p><strong>DIÁLOGO. LA PÁLABRA COMO ACCIÓN</strong> </p>
             <p>¿Cómo hablan    nuestros personajes? </p>
             <p>Diferenciar a    cada uno de ellos desde la palabra. </p>
             <p>La    importancia de la identidad en la construcción de cada uno. </p>
             <p>Como mueve la    palabra a la historia y a los personajes, partiendo de la base que la    dramaturgia se construye desde la palabra. </p></td>
