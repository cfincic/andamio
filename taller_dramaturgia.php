                   <br />
                   <br />
                 ¿Por qué esa historia se cuenta en ese    día y no en otro?<br />
                 <br />
                 <br />
                 Desde que punto de vista la cuento. <br />
                 <br />
                 <br />
                 Tomar partido de los diversos puntos de    vista de los personajes.</p></td>
