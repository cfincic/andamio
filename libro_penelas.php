           <div id="yui_3_2_0_1_1336486970801342"><span id="yui_3_2_0_1_1336486970801339">
             <div><br />
                 </div>
             <div>&quot;La   poesía de Lisandro Penelas rehuye lo sublime, lo absoluto, la abstracta   universalidad y sin embargo, aferrándose a su capacidad de canto y a   cierta plenitud, roza las grandes preocupaciones del hombre, esos “Pocos misterios. / El mar, el cielo, el tiempo, / el amor, (su) madre, (su) padre, / la luz y el milagroso prodigio del sol”, como los nombra, sin poderles   quitar su “mucha   grandeza, sublimidad y universalidad”.&quot; Luis Alberto Salvarezza<br />
             </div>
             <div><br />
                 </div>
             <div><br />
             </div>
             <div>Sobre el autor</div>
             <div><br />
                 </div>
             <div>Lisandro Penelas nació en Buenos Aires en 1983. Es actor, director y profesor de actuación recibido de CoSATyC. Trabajó en más de veinte obras de teatro, y en varios cortos y largometrajes. Se formó con   docentes como Claudio Tolcachir, Luciano Suardi, Javier Daulte y   Marcelo Katz, entre otros, y en 2009 fue becado para formar parte del Cultural Visitors Program for Directors en Estados Unidos. </div>
             <div><br />
             </div>
             <div>Paralelamente   a su trabajo en el mundo de la escena estuvo siempre en contacto con su   otra vocación: la escritura. Cursó la carrera de Artes Combinadas en la   Universidad de Buenos Aires. En 2000 publicó su primer cuento, Ubi sunt?, por Ediciones del valle y con sólo veinte años su cuento La foto fue   seleccionado por Igriega Movimiento Cultural para formar parte de una   antología de relatos publicada en Sevilla, España. Ese mismo año su   estudio   Chaplin: la commedia dell’arte llega al cine integró el libro ¿Imágenes del nuevo siglo? de Editorial TEALHI. En 2006 colaboró en la redacción de varios volúmenes de la colección Protagonistas de la Cultura que publicara el Diario La Nación junto a Editorial Aguilar. En 2009 su poema Ana fue incluido en El libro de los talleres de Editorial Dunken. Como dramaturgo, realizó la adaptación teatral de ¿Querés hacer el favor de callarte, por favor? basada en tres relatos de Raymond Carver.</div>
             <div><br />
             </div>
             <div>La   publicación de este libro le da la bienvenida a este joven   escritor argentino, que presenta aquí su primer poemario, donde puede   reconocerse un amplio abanico de influencias: la generación del 27, los   autores estadounidenses contemporáneos y la literatura argentina y   latinoamericana del último siglo. </div>
