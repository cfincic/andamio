       <div>Primer libro de poemas de Lisandro Penelas</div>
       <div><br />
       </div>
       <div> <br />
       </div>
       <div id="yui_3_2_0_1_1336486970801272">
         <div id="yui_3_2_0_1_1336486970801269"><span id="yui_3_2_0_1_1336486970801266"><span id="yui_3_2_0_1_1336486970801263"><img src="images/libro_penelas.jpg" alt="" name="yui_3_2_0_1_1336486970801260" id="yui_3_2_0_1_1336486970801260" /> </span><br />
